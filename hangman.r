width <- 20
height <- 20
word <- ""

getindex <- function(strng,chr){
	res = c()
	i = 0
	for (c in strsplit(strng, "")[[1]]) {
		i=i+1
		if(c == chr){
			res <- c(res,i)
		}
	}
	return(res)
}

printscreen <- function(){
	for(x in 1:height){
		for(y in 1:width){
			item <- screen[x + height*y]
			cat(item,sep="")
		}
		cat("\n")
	}
}

getword <- function(){
	cat("choose a word to play with: ")
	word <- readLines("stdin",1)
	return(word)
}

coord <- function(x,y){
	return(x+y*width)
}

sample1 <- function(file, n) {
  lf <- laf_open(detect_dm_csv(file, sep = ",", header = TRUE, factor_fraction = -1))
  return(read_lines(lf, sample(1:nrow(lf), n)))
}

start <- function(){
	lineNumber = sample(142500, 2)
	word <<- scan("words.txt", what = "string", skip=lineNumber[1]-1, nlines=1)

	# word <<- getword()
	tries = 0

	cat("\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n")

	guessword <<- replicate(nchar(word), "_")

	while(TRUE){
		printscreen()
		cat(guessword,"\n")

		c <- readChar("stdin",1)
		index <- getindex(word,c)

		if(length(index) == 0){
			tries = tries + 1
			if(tries == 1){
				for (i in 0:height) {
					screen[i*width] <<- "_"
				}
			}else if(tries == 2){
				for (i in 1:width) {
					screen[i+(3)*width] <<- "|"
				}
			}else if(tries == 3){
				for (i in 3:12) {
					screen[1 + i*width] <<- "_"
				}
			}else if(tries == 4){
				screen[2 + 4*width] <<- "/"
			}else if(tries == 5){
				screen[2 + 12*width] <<- "|"
			}else if(tries == 6){
				screen[coord(3,12)] <<- "-"
				screen[coord(3,11)] <<- "/"
				screen[coord(3,13)] <<- "\\"
				screen[coord(4,11)] <<- "\\"
				screen[coord(4,13)] <<- "/"
				screen[coord(4,12)] <<- "_"
			}else if(tries == 7){
				for (i in 5:12) {
					screen[coord(i,12)] <<- "|"
				}
			}else if(tries == 8){
				screen[coord(7,13)] <<- "/"
				screen[coord(6,14)] <<- "/"
				screen[coord(5,15)] <<- "/"
			}else if(tries == 9){
				screen[coord(7,11)] <<- "\\"
				screen[coord(6,10)] <<- "\\"
				screen[coord(5,9)] <<- "\\"
			}else if(tries == 10){
				screen[coord(12,13)] <<- "\\"
				screen[coord(13,14)] <<- "\\"
				screen[coord(14,15)] <<- "\\"
			}else if(tries == 11){
				screen[coord(12,11)] <<- "/"
				screen[coord(13,10)] <<- "/"
				screen[coord(14,9)] <<- "/"
			}else{
				cat("game over\n")
				cat("it was: ")
				cat(word)
				return()
			}

		}else{
			for(i in index){
				guessword[i] = c
			}
			if(paste(guessword,collapse="") == word){
				cat("well done\n")
				return()
			}
		}
	}
}


